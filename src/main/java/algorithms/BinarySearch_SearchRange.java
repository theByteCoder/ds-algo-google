    public static int searchRange(final List<Integer> arr, int target, boolean isStartingIndex) {
        int index = -1;
        int left = 0;
        int right = arr.size() - 1;

        while (left <= right) {
            int mid = left + (right - left) / 2;
            if (arr.get(mid) == target) index = mid;
            if (isStartingIndex) {
                if (arr.get(mid) >= target) right = mid - 1;
                else left = mid + 1;
            } else {
                if (arr.get(mid) <= target) left = mid + 1;
                else right = mid - 1;
            }
        }
        return index;
    }

